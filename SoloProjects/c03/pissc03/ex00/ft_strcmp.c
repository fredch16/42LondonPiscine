#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	result;

	while (*s1 != '\0' && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	result = *s1 - *s2;
	return (result);
}
